package source02;

public class Test09_String {

	public static void main(String[] args) {
		String str = "난 \"자바\"를 좋아합니다.";
		System.out.println(str);
		
		String tab = "번호\t이름\t나이";
		System.out.println(tab);
		
		String line = "장나라\n나라장";
		System.out.println(line);
		
		String backslash = "봄\\여름\\가을\\겨울";
		System.out.println(backslash);
	}

}
