package source13.java_api.exam01;

public class Test02 {

	public static void main(String[] args) {
		// 2의 8승 256.0 출력
		System.out.println("Math.pow(2, 8) : " + Math.pow(2, 8));
		
		// 0.0이상 1.0 미만의 난수 출력
		System.out.println("Math.random() : " + Math.random());
		
		// 40과 30중에서 작은 값 30 출력
		System.out.println("Math.min() : " + Math.min(40, 30));
		
		// 40과 30중에서 큰 값 40출력
		System.out.println("Math.max() : " + Math.max(40, 30));
	}

}
