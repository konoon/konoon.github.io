package source03_add;

public class Test03 {

	public static void main(String[] args) {
		
		int a = 10;
		int b = 20;
		int c = 30;
		int d = 40;
		
		a++; // a = a + 1;
		System.out.println("a = " + a);
		
		b--; // b = b - 1;
		System.out.println("b = " + b);

		++c;
		System.out.println("c = " + c);
		
		--d;
		System.out.println("d = " + d);
	}

}
