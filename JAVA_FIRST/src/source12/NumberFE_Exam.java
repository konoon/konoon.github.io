package source12;

public class NumberFE_Exam {

	public static void main(String[] args) {
		String data1 = "100";
		String data2 = "a100";
		
		int value1 = Integer.parseInt(data1);
		int value2 = Integer.parseInt(data2);
		
		System.out.println(value1); // int형 100으로 변환 출력됨
		System.out.println(value2);
	}

}
